<script lang="ts" context="module">
    export const showCreateFile = () => {
        wizard.start(Create);
    };
</script>

<script lang="ts">
    import { invalidate } from '$app/navigation';
    import { base } from '$app/paths';
    import { page } from '$app/stores';
    import { Submit, trackError, trackEvent } from '$lib/actions/analytics';
    import {
        Avatar,
        DropList,
        DropListItem,
        DropListLink,
        Empty,
        EmptySearch,
        PaginationWithLimit,
        SearchQuery
    } from '$lib/components';
    import { Dependencies } from '$lib/constants';
    import { Pill } from '$lib/elements';
    import { Button } from '$lib/elements/forms';
    import { toLocaleDate } from '$lib/helpers/date';
    import {
        bytesToSize,
        calculateSize,
        humanFileSize,
        sizeToBytes
    } from '$lib/helpers/sizeConvertion';
    import { Container } from '$lib/layout';
    import type { Models } from '@appwrite.io/console';
    import { addNotification } from '$lib/stores/notifications';
    import { uploader } from '$lib/stores/uploader';
    import { wizard } from '$lib/stores/wizard';
    import { getServiceLimit } from '$lib/stores/billing';
    import { sdk } from '$lib/stores/sdk.js';
    import DeleteFile from './deleteFile.svelte';
    import { isCloud } from '$lib/system';
    import { Layout, Table, Icon } from '@appwrite.io/pink-svelte';
    import { onMount } from 'svelte';
    import { IconPlus } from '@appwrite.io/pink-icons-svelte';

    export let data;

    let showDelete = false;
    let showDropdown = [];
    let selectedFile: Models.File = null;

    const projectId = $page.params.project;
    const bucketId = $page.params.bucket;
    const usedStorage =
        isCloud && data?.organizationUsage?.storageTotal
            ? bytesToSize(data.organizationUsage.storageTotal, 'GB')
            : null;
    const getPreview = (fileId: string) =>
        sdk.forProject.storage.getFilePreview(bucketId, fileId, 128, 128).toString() +
        '&mode=admin';

    async function fileDeleted(event: CustomEvent<Models.File>) {
        showDelete = false;
        uploader.removeFile(event.detail);
        await invalidate(Dependencies.FILES);
    }

    async function deleteFile(file: Models.File) {
        try {
            await sdk.forProject.storage.deleteFile(file.bucketId, file.$id);
            await invalidate(Dependencies.FILES);
            uploader.removeFile(file);
            trackEvent(Submit.FileDelete);
        } catch (error) {
            addNotification({
                type: 'error',
                message: error.message
            });
            trackError(error, Submit.FileDelete);
        }
    }

    $: maxFileSize = isCloud
        ? humanFileSize(sizeToBytes(getServiceLimit('fileSize'), 'MB', 1000))
        : null;

    let isUploading = false;

    const beforeunload = (event: BeforeUnloadEvent) => {
        // legacy browser **may** support showing a custom message.
        const message = 'An upload is in progress. Are you sure you want to leave?';

        if (isUploading) {
            event.preventDefault();
            event.returnValue = message;
            return message;
        }
    };

    onMount(() => {
        return uploader.subscribe(() => {
            isUploading = $uploader.files.some(
                (file) => !file.completed && file.progress < 100 && !file.failed
            );
        });
    });
</script>

<svelte:window on:beforeunload={beforeunload} />

<Container>
    <Layout.Stack direction="row" justifyContent="space-between">
        <Layout.Stack direction="row" alignItems="center">
            <SearchQuery search={data.search} placeholder="Search files" />
        </Layout.Stack>
        <Layout.Stack direction="row" alignItems="center" justifyContent="flex-end">
            <Button
                href={`${base}/project-${$page.params.project}/storage/bucket-${$page.params.bucket}/create`}
                event="create_file"
                size="s">
                <Icon icon={IconPlus} slot="start" size="s" />
                Create file
            </Button>
        </Layout.Stack>
    </Layout.Stack>

    {#if data.files.total}
        <Table.Root>
            <svelte:fragment slot="header">
                <Table.Header.Cell>Filename</Table.Header.Cell>
                <Table.Header.Cell width="140px">Type</Table.Header.Cell>
                <Table.Header.Cell width="100px">Size</Table.Header.Cell>
                <Table.Header.Cell width="120px">Created</Table.Header.Cell>
                <Table.Header.Cell width="40px" />
            </svelte:fragment>
            {#each data.files.files as file, index}
                {#if file.chunksTotal / file.chunksUploaded !== 1}
                    <Table.Row>
                        <Table.Cell>
                            <Layout.Stack direction="row" alignItems="center">
                                <span class="avatar is-size-small is-color-empty" />
                                <span class="text u-trim">{file.name}</span>
                                <div>
                                    <Pill warning>Pending</Pill>
                                </div>
                            </Layout.Stack>
                        </Table.Cell>
                        <Table.Cell>{file.mimeType}</Table.Cell>
                        <Table.Cell>
                            {calculateSize(file.sizeOriginal)}
                        </Table.Cell>
                        <Table.Cell>
                            {toLocaleDate(file.$createdAt)}
                        </Table.Cell>
                        <Table.Cell>
                            <div class="u-flex u-main-center">
                                <button
                                    class="button is-only-icon is-text"
                                    aria-label="Delete item"
                                    on:click|preventDefault={() => {
                                        deleteFile(file);
                                    }}>
                                    <span class="icon-trash" aria-hidden="true" />
                                </button>
                            </div>
                        </Table.Cell>
                    </Table.Row>
                {:else}
                    <Table.Link
                        href={`${base}/project-${projectId}/storage/bucket-${bucketId}/file-${file.$id}`}>
                        <Table.Cell>
                            <div class="u-flex u-gap-12 u-cross-center">
                                <Avatar size="xs" src={getPreview(file.$id)} alt={file.name} />
                                <span class="text u-trim">{file.name}</span>
                            </div>
                        </Table.Cell>
                        <Table.Cell>{file.mimeType}</Table.Cell>
                        <Table.Cell>
                            {calculateSize(file.sizeOriginal)}
                        </Table.Cell>
                        <Table.Cell>
                            {toLocaleDate(file.$createdAt)}
                        </Table.Cell>
                        <Table.Cell>
                            <DropList
                                bind:show={showDropdown[index]}
                                placement="bottom-start"
                                noArrow>
                                <button
                                    class="button is-only-icon is-text"
                                    aria-label="More options"
                                    on:click|preventDefault={() => {
                                        showDropdown[index] = !showDropdown[index];
                                    }}>
                                    <span class="icon-dots-horizontal" aria-hidden="true" />
                                </button>
                                <svelte:fragment slot="list">
                                    <DropListLink
                                        on:click={() => {
                                            showDropdown[index] = false;
                                        }}
                                        href={`${base}/project-${projectId}/storage/bucket-${bucketId}/file-${file.$id}`}
                                        icon="pencil">Update</DropListLink>
                                    <DropListItem
                                        icon="trash"
                                        on:click={() => {
                                            showDropdown[index] = false;
                                            selectedFile = file;
                                            showDelete = true;
                                        }}>Delete</DropListItem>
                                </svelte:fragment>
                            </DropList>
                        </Table.Cell>
                    </Table.Link>
                {/if}
            {/each}
        </Table.Root>

        <PaginationWithLimit
            name="Files"
            limit={data.limit}
            offset={data.offset}
            total={data.files.total} />
    {:else if data.search}
        <EmptySearch>
            <div class="u-text-center">
                <b>Sorry, we couldn't find '{data.search}'</b>
                <p>There are no files that match your search.</p>
            </div>
            <div class="u-flex u-gap-16">
                <Button
                    external
                    href="https://appwrite.io/docs/products/storage/upload-download"
                    text>
                    Documentation
                </Button>
                <Button
                    secondary
                    href={`${base}/project-${$page.params.project}/storage/bucket-${$page.params.bucket}`}>
                    Clear Search
                </Button>
            </div>
        </EmptySearch>
    {:else}
        <Empty
            single
            href="https://appwrite.io/docs/products/storage/upload-download"
            target="file"
            on:click={() => void 0} />
    {/if}
</Container>

{#if selectedFile}
    <DeleteFile file={selectedFile} bind:showDelete on:deleted={fileDeleted} />
{/if}
