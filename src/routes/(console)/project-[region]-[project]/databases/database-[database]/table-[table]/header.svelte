<script lang="ts">
    import { base } from '$app/paths';
    import { page } from '$app/state';
    import { Id, Tab, Tabs } from '$lib/components';
    import { isTabSelected } from '$lib/helpers/load';
    import { Cover, CoverTitle } from '$lib/layout';
    import { canWriteTables } from '$lib/stores/roles';
    import { table } from './store';
    import { isTabletViewport } from '$lib/stores/viewport';

    const databaseId = $derived(page.params.database);

    const tableId = $derived(page.params.table);

    const path = $derived(
        `${base}/project-${page.params.region}-${page.params.project}/databases/database-${databaseId}/table-${tableId}`
    );

    const tabs = $derived(
        [
            {
                href: path,
                title: 'Rows',
                event: 'rows',
                hasChildren: true
            },
            {
                href: `${path}/columns`,
                title: 'Columns',
                event: 'columns'
            },
            {
                href: `${path}/indexes`,
                title: 'Indexes',
                event: 'indexes'
            },
            {
                href: `${path}/activity`,
                title: 'Activity',
                event: 'activity',
                hasChildren: true
            },
            {
                href: `${path}/usage`,
                title: 'Usage',
                event: 'usage',
                hasChildren: true
            },
            {
                href: `${path}/settings`,
                title: 'Settings',
                event: 'settings',
                disabled: !$canWriteTables
            }
        ].filter((tab) => !tab.disabled)
    );
</script>

<div style:margin-top={$isTabletViewport ? '48px' : 0}>
    <Cover>
        <svelte:fragment slot="header">
            <CoverTitle
                href={`${base}/project-${page.params.region}-${page.params.project}/databases/database-${databaseId}`}>
                {$table?.name}
            </CoverTitle>
            {#key $table?.$id}
                <Id value={$table?.$id}>{$table?.$id}</Id>
            {/key}
        </svelte:fragment>

        <Tabs>
            {#each tabs as tab}
                <Tab
                    href={tab.href}
                    selected={isTabSelected(tab, page.url.pathname, path, tabs)}
                    event={tab.event}>
                    {tab.title}
                </Tab>
            {/each}
        </Tabs>
    </Cover>
</div>
