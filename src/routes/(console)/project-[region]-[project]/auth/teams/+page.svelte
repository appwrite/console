<script lang="ts" context="module">
    export let showCreateTeam = writable(false);
    export let showDeleteAllTeams = writable(false);
</script>

<script lang="ts">
    import { page } from '$app/state';
    import { Button } from '$lib/elements/forms';
    import {
        Empty,
        EmptySearch,
        AvatarInitials,
        SearchQuery,
        PaginationWithLimit
    } from '$lib/components';
    import Create from '../createTeam.svelte';
    import DeleteAllTeams from './deleteAllTeams.svelte';
    import { goto } from '$app/navigation';
    import { Container } from '$lib/layout';
    import { base } from '$app/paths';
    import type { Models } from '@appwrite.io/console';
    import { writable } from 'svelte/store';
    import { canWriteTeams } from '$lib/stores/roles';
    import { Icon, Layout, Table } from '@appwrite.io/pink-svelte';
    import { IconPlus } from '@appwrite.io/pink-icons-svelte';
    import DualTimeView from '$lib/components/dualTimeView.svelte';

    export let data;

    const region = page.params.region;
    const project = page.params.project;
    const teamCreated = async (event: CustomEvent<Models.Team<Record<string, unknown>>>) => {
        await goto(`${base}/project-${region}-${project}/auth/teams/team-${event.detail.$id}`);
    };
</script>

<Container>
    <Layout.Stack direction="row" justifyContent="space-between">
        <Layout.Stack direction="row" alignItems="center">
            <SearchQuery placeholder="Search by name" />
        </Layout.Stack>
        <Layout.Stack direction="row" alignItems="center" justifyContent="flex-end">
            {#if data.teams.total}
                <Button secondary on:click={() => ($showDeleteAllTeams = true)} size="s">
                    <span class="text">Delete All</span>
                </Button>
            {/if}
            <Button on:mousedown={() => ($showCreateTeam = true)} event="create_user" size="s">
                <Icon icon={IconPlus} slot="start" size="s" />
                Create team
            </Button>
        </Layout.Stack>
    </Layout.Stack>

    {#if data.teams.total}
        <Table.Root columns={3} let:root>
            <svelte:fragment slot="header" let:root>
                <Table.Header.Cell {root}>Name</Table.Header.Cell>
                <Table.Header.Cell {root}>Members</Table.Header.Cell>
                <Table.Header.Cell {root}>Created</Table.Header.Cell>
            </svelte:fragment>
            {#each data.teams.teams as team}
                <Table.Row.Link
                    {root}
                    href={`${base}/project-${region}-${project}/auth/teams/team-${team.$id}`}>
                    <Table.Cell {root}>
                        <Layout.Stack direction="row" alignItems="center">
                            <AvatarInitials size="xs" name={team.name} />
                            <span class="u-trim">{team.name}</span>
                        </Layout.Stack>
                    </Table.Cell>
                    <Table.Cell {root}>
                        {team.total} members
                    </Table.Cell>
                    <Table.Cell {root}>
                        <DualTimeView time={team.$createdAt} />
                    </Table.Cell>
                </Table.Row.Link>
            {/each}
        </Table.Root>

        <PaginationWithLimit
            name="Teams"
            limit={data.limit}
            offset={data.offset}
            total={data.teams.total} />
    {:else if data.search}
        <EmptySearch target="teams" search={data.search} hidePagination={data.teams.total === 0}>
            <Button secondary size="s" href={`${base}/project-${region}-${project}/auth/teams`}>
                Clear Search
            </Button>
        </EmptySearch>
    {:else}
        <Empty
            single
            allowCreate={$canWriteTeams}
            on:click={() => ($showCreateTeam = true)}
            href="https://appwrite.io/docs/references/cloud/client-web/teams"
            target="team" />
    {/if}
</Container>

<Create bind:showCreate={$showCreateTeam} on:created={teamCreated} />
<DeleteAllTeams bind:showDeleteAll={$showDeleteAllTeams} />
